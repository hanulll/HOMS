def print_recipe(
    menu_name,
):

    recipe = get_recipe(
        menu_name,
    )

    print()
    print("=" * 60)
    print(menu_name)
    print("-" * 60)

    if recipe:

        for ingredient, amount in recipe.items():

            print(
                f"{ingredient:<15} {amount}"
            )

    else:

        print(
            "레시피 없음"
        )

    print("=" * 60)


