def validate_recipe():

    """
    recipe_rules.py 전체 검증
    """

    for menu_name, recipe in RECIPE_RULES.items():

        if not isinstance(recipe, dict):
            raise ValueError(f"{menu_name} : recipe must be dict")

        for ingredient, amount in recipe.items():

            ingredient = normalize_ingredient(ingredient)

            if amount < 0:
                raise ValueError(
                    f"{menu_name} : {ingredient} amount must be positive"
                )

    return True


