from collections import defaultdict


def create_recipe_dict():

    """
    메뉴 판매량으로부터
    원재료 사용량 집계 dict 생성
    """

    return defaultdict(float)


def calculate_recipe(menu_name: str, quantity: float = 1):

    """
    메뉴 판매수량(quantity)에 따른
    원재료 사용량 계산
    """

    result = defaultdict(float)

    recipe = get_recipe(menu_name)

    if not recipe:
        return result

    for ingredient, amount in recipe.items():

        ingredient = normalize_ingredient(ingredient)

        result[ingredient] += amount * quantity

    return result


# ==========================================================
# 메뉴 여러 건 집계
# ==========================================================

def merge_recipe(result_dict, menu_name, quantity):

    """
    하나의 메뉴를
    집계 Dict에 합산
    """

    recipe = calculate_recipe(menu_name, quantity)

    for ingredient, amount in recipe.items():

        result_dict[ingredient] += amount

    return result_dict


def calculate_sales_recipe(sales_dict):

    """
    sales_dict

    {
        "허니콤보":120,
        "교촌윙":35,
        ...
    }

    →

    원재료 사용량 반환
    """

    result = create_recipe_dict()

    for menu_name, quantity in sales_dict.items():

        merge_recipe(result, menu_name, quantity)

    return result
# ==========================================================
# 단위 변환
# ==========================================================

def convert_to_kg(gram: float) -> float:
    """
    g → kg
    """
    return gram / 1000.0


def convert_recipe_to_kg(recipe_dict):

    """
    북채 / 날개는 g → kg 변환
    나머지는 그대로 사용
    """

    result = {}

    for ingredient, amount in recipe_dict.items():

        if ingredient in ("북채", "날개"):
            result[ingredient] = convert_to_kg(amount)
        else:
            result[ingredient] = amount

    return result


# ==========================================================
# 최종 사용량 계산
# ==========================================================

def calculate_total_recipe(sales_dict):

    """
    판매내역 →

    원재료 사용량(kg / pack)
    """

    usage = calculate_sales_recipe(sales_dict)

    usage = convert_recipe_to_kg(usage)

    return usage


# ==========================================================
# 원재료 사용량 조회
# ==========================================================

def get_used_amount(sales_dict, ingredient):

    ingredient = normalize_ingredient(ingredient)

    usage = calculate_total_recipe(sales_dict)

    return usage.get(ingredient, 0)
