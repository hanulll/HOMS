def get_recipe(menu_name: str):

    """
    메뉴 레시피 반환
    """

    return RECIPE_RULES.get(menu_name, {})


def has_recipe(menu_name: str) -> bool:

    """
    레시피 존재 여부
    """

    return menu_name in RECIPE_RULES


def get_recipe_ingredients(menu_name: str):

    """
    메뉴에 사용되는 원재료 목록
    """

    return list(get_recipe(menu_name).keys())


def get_recipe_amount(menu_name: str, ingredient: str):

    """
    특정 원재료 사용량 반환
    """

    ingredient = normalize_ingredient(ingredient)

    recipe = get_recipe(menu_name)

    return recipe.get(ingredient, 0)


