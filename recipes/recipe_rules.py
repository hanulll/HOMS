# Recipe Lookup
# ==========================================================

def get_recipe(
    menu_name,
):

    menu_type = get_menu_type(
        normalize_menu(
            menu_name,
        )
    )

    if menu_type is None:

        return {}

    return deepcopy(
        BASE_RECIPES.get(
            menu_type,
            {},
        )
    )
